      notice_type: "Article",
      notice_id: article,
      application_id: device_info.application_id,
      user_id: device_info.baidu_user_id,
      channel_id: device_info.baidu_channel_id,
      push_type: 1,
      message_type: 1,
      title: "0元购三天，您还等神马？！",
      description: "0元带性福回家，只对新用户只限3天！再不抢就没啦！！！"
    }
